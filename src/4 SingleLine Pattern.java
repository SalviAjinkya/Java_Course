class SingleLine{
    public static void main(String[] args) {
        System.out.println("*\n**\n***\n****\n*****");

        //here we add \n at the start also, so that there is space between our patterns
        System.out.println("\n*****\n****\n***\n**\n*");

        //here we add \n at the start also, so that there is space between our patterns
        System.out.println("\n    *\n   **\n  ***\n ****\n*****");
    }
}